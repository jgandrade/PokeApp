export function bgType(type: string): string {
  if (type === "grass") {
    return "after:bg-[#5CBE64]";
  }

  if (type === "water") {
    return "after:bg-[#539CDF]";
  }

  if (type === "fire") {
    return "after:bg-[#FF7709]";
  }

  if (type === "bug") {
    return "after:bg-[#98BE2E]";
  }

  if (type === "dark") {
    return "after:bg-[#5F606D]";
  }

  if (type === "dragon") {
    return "after:bg-[#0773C7]";
  }

  if (type === "electric") {
    return "after:bg-[#EDD53F]";
  }

  if (type === "fairy") {
    return "after:bg-[#EF97E6]";
  }

  if (type === "fight") {
    return "after:bg-[#D94256]";
  }

  if (type === "flying") {
    return "after:bg-[#9BB4E8]";
  }

  if (type === "ghost") {
    return "after:bg-[#6970C5]";
  }

  if (type === "ground") {
    return "after:bg-[#D78555]";
  }

  if (type === "ice") {
    return "after:bg-[#7ED4C9]";
  }

  if (type === "normal") {
    return "after:bg-[#9A9DA1]";
  }

  if (type === "poison") {
    return "after:bg-[#B563CE]";
  }

  if (type === "psychic") {
    return "after:bg-[#F87C7A]";
  }

  if (type === "steel") {
    return "after:bg-[#5596A4]";
  }

  if (type === "rock") {
    return "after:bg-[#CEC18C]";
  }

  return "after:bg-[#666666]";
}

